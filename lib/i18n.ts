export function normalizeLanguage(value?: string | null): LanguageCode {
  const normalized = value?.toLowerCase().split("-")[0];
  return languages.some((language) => language.code === normalized) ? (normalized as LanguageCode) : defaultLanguage;
}

export function getLanguageDirection(language: LanguageCode) {
  return languages.find((item) => item.code === language)?.dir ?? "ltr";
}

